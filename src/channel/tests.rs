#[test]
fn test_decode_bytes() {
    for (channel, bytes) in default_pmr_channels()
        .into_iter()
        .zip(default_pmr_bytes().into_iter())
    {
        let decoded_channel: Option<Channel> = bytes.as_slice().try_into().ok();
        assert_eq!(channel, decoded_channel)
    }
}
